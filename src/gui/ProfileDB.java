    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     JFileChooser fileChooser = new JFileChooser();
        fileChooser.showOpenDialog(null);
        File f = fileChooser.getSelectedFile();
        path = f.getAbsolutePath();
        
        String imageName = f.getName();
        String destinationPath = "C:\\Users\\barth\\Documents" + imageName;
        
        try {
        
            BufferedImage bi = ImageIO.read(new File(path));
            Image img = bi.getScaledInstance(300, 300,Image.SCALE_SMOOTH);
            ImageIcon ii = new ImageIcon(img);
            jLabel2.setIcon(ii);
        
            File destination = new File(destinationPath);
        Files.copy(f.toPath(), destination.toPath(), StandardCopyOption.REPLACE_EXISTING);
        
        } catch (IOException ex) {
            Logger.getLogger(ProfileDB.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            
       
        String name = jTextField1.getText(); 
        String mobile = jTextField2.getText();
        String address = jTextField3.getText();
        
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter employee first name", "Warning", JOptionPane.WARNING_MESSAGE);
            
         
            }else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter employee mobile", "Warning", JOptionPane.WARNING_MESSAGE);
            
            } else if (!mobile.matches("^07[012345678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Please enter employee valid mobile", "Warning", JOptionPane.WARNING_MESSAGE);
            
            }else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter employee address ", "Warning", JOptionPane.WARNING_MESSAGE);
            
           
            }else{
                
               ResultSet resultSet = MySQL.executeSearch("SELECT * FROM distributor WHERE mobile='" + mobile + "'");

                boolean canUpdate = false;

                if (resultSet.next()) {
                    if (!resultSet.getString("mobile").equals(mobile)) {
                        JOptionPane.showMessageDialog(this, "This mobile is already in use", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        canUpdate = true;
                    }
                } else {
                    canUpdate = true;
                }
                if (canUpdate) {
                    String updateQuery = "UPDATE distributor SET "
        + "name = '" +name + "', "
     
        + "address = '" + address + "'"
        + "WHERE mobile = '" + mobile + "'";
       

                    MySQL.executeIUD(updateQuery);
                    JOptionPane.showMessageDialog(this, "Update Successfully", "Warning", JOptionPane.WARNING_MESSAGE);
                    
                }
            

            }

        } catch (Exception e) {
            e.printStackTrace(); 
        }

        
    }//GEN-LAST:event_jButton1ActionPerformed

