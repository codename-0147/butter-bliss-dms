   private void loadTable(){
         
         try {
              ResultSet resultset = MySQL.executeSearch("SELECT * FROM `order` INNER JOIN `product`"
                    + " ON `product`.`id` = `order`.`product_id`  ");
           
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            
            while (resultset.next()) {
                
                Vector<String> vector = new Vector<>();
                vector.add(resultset.getString("order.id"));
                vector.add(resultset.getString("product.name"));
                vector.add(resultset.getString("order.date"));
                vector.add(resultset.getString("outlet.name"));
                vector.add(resultset.getString("delivery.name"));
                
                
                model.addRow(vector);
            }
         } catch (Exception e) {
             e.printStackTrace();
         }
     }
