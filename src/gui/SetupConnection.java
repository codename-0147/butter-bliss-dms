    private void selectDumpFileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectDumpFileButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Select mysqldump Executable");
        int userSelection = fileChooser.showOpenDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            dumpFilePathField.setText(file.getAbsolutePath());
        }
    }//GEN-LAST:event_selectDumpFileButtonActionPerformed

    private void setupConnectionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setupConnectionButtonActionPerformed
        String host = hostAddressField.getText().trim();
        String port = portField.getText().trim();
        String dbName = dbNameField.getText().trim();
        String username = usernameField.getText().trim();
        String password = new String(passwordField.getPassword());
        String mysqldumpPath = dumpFilePathField.getText().trim();
        
        if (host.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a host address", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (port.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a port", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (dbName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a database name", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (username.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a username", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a password", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (mysqldumpPath.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select the mysqldump file", "Warning", JOptionPane.WARNING_MESSAGE);
        }else {
            MySQLNew db = new MySQLNew();
            db.host = host;
            db.port = port;
            db.dbname = dbName;
            db.un = username;
            db.pw = password;
            db.dump = mysqldumpPath;
            
            try {
                String appPath = new File("").getAbsolutePath();
                File dbInfoFile = new File(appPath + File.separator + "dbinfo.ser");

                ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream(dbInfoFile));
                oos.writeObject(db);
                oos.close();
                
                db.createConnection();
                JOptionPane.showMessageDialog(this, "Connection setup successfully.");
                dispose();
                //java.awt.EventQueue.invokeLater(() -> new SignIn().setVisible(true));
                java.awt.EventQueue.invokeLater(() -> new WHome().setVisible(true));
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error saving connection info.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_setupConnectionButtonActionPerformed

