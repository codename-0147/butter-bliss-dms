    private void loadOutlets(){
        
        try {
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `outlet`"
                    + "INNER JOIN `outlet_manager` ON `outlet`.`outlet_manager_id`=`outlet_manager`.`id`");
            
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            
            while (resultSet.next()) {
               Vector<String> vector = new Vector<>();
               vector.add(resultSet.getString("id"));
               vector.add(resultSet.getString("name"));
               vector.add(resultSet.getString("mobile"));
               vector.add(resultSet.getString("address"));
               vector.add(resultSet.getString("outlet_manager.username"));
               model.addRow(vector);
                
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void loadManager(){
        
        try {
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `outlet_manager`");
            
            Vector<String> vector = new Vector<>();
            vector.add("Select");
            
            while (resultSet.next()) {
            vector.add(resultSet.getString("username"));
            managerMap.put(resultSet.getString("username"),resultSet.getString("id"));
            
                
            }
            
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

