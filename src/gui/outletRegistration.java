    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            
            String outlet = jTextField2.getText();
            String mobile = jTextField3.getText();
            String address = jTextField1.getText();
            String outletManager = String.valueOf(jComboBox1.getSelectedItem());
            
            if (outlet.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Outlet Name","Warning",JOptionPane.WARNING_MESSAGE);
                
            } else if (mobile.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Your Mobile","Warning",JOptionPane.WARNING_MESSAGE);
                
            } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
                JOptionPane.showMessageDialog(this, "Please Enter valid mobile", "Warning", JOptionPane.WARNING_MESSAGE);
                
            }else if (address.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter address","Warning",JOptionPane.WARNING_MESSAGE);
                
            }else if (outletManager.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please Select Manager","Warning",JOptionPane.WARNING_MESSAGE);
                
            }else  {
                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `outlet` WHERE `mobile` ='"+mobile+"'");
                
                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "This Outlet Already Registered","Warning",JOptionPane.WARNING_MESSAGE);
                    
                } else {
                    MySQL.executeIUD("INSERT INTO `outlet` (`name`,`mobile`,`address`,`outlet_manager_id`)"
                            + "VALUES('"+outlet+"','"+mobile+"','"+address+"',"
                            + "'"+managerMap.get(outletManager)+"')");
                    JOptionPane.showMessageDialog(this, "New Outlet registered","Warning",JOptionPane.WARNING_MESSAGE);
                    
                    loadOutlets();
                }
           
                 
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
       
    }//GEN-LAST:event_jButton5ActionPerformed

