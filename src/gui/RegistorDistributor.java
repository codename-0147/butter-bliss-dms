    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            
           
            String name = jTextField2.getText();
            String username = jTextField3.getText();
            String password = jTextField4.getText();
            String email = jTextField1.getText();
            String nic = jTextField5.getText();
            String mobile = jTextField6.getText();
            String address = jTextField7.getText();
            String licenseno = jTextField8.getText();
           Date date = jDateChooser1.getDate();
             SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
             String formattedDate = (date != null) ? sdf.format(date) : null;
            String vehicleno = jTextField10.getText();

            String imageName = path != null ? new File(path).getName() : "";

          
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Your full name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (username.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Your Username", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Your Password", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!validatePassword(password)) {
                JOptionPane.showMessageDialog(this, "Password must contain at least one digit, one uppercase letter, one lowercase letter, and one special character.", "Warning", JOptionPane.WARNING_MESSAGE);
              
            } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9_-]+(\\.[A-Za-z0-9_-]+)*@" 
            + "[^-][A-Za-z0-9-]+(\\.[A-Za-z0-9-]+)*(\\.[A-Za-z]{2,})$")) {
            
            JOptionPane.showMessageDialog(this, "Please enter employee valid email", "Warning", JOptionPane.WARNING_MESSAGE);
          
            } else if (nic.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Your NIC", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (mobile.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Your mobile", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!mobile.matches("^07[012345678]{1}[0-9]{7}$")) {
                JOptionPane.showMessageDialog(this, "Please enter a valid mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (address.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Your address", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (licenseno.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Your license number", "Warning", JOptionPane.WARNING_MESSAGE);
            }else if (formattedDate == null) {                           
            JOptionPane.showMessageDialog(this, "Please enter Your join date", "Error", JOptionPane.WARNING_MESSAGE);
            } else if (vehicleno.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Your vehicle number", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
               
                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `distributor` WHERE `mobile` = '" + mobile + "' OR `vehicle_no` = '" + vehicleno + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Distributor already registered", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                  
                    MySQL.executeIUD("INSERT INTO `distributor`(`name`, `mobile`, `vehicle_no`, `address`, `joined_date`, `nic`, `username`, `password`, `imagename`, `license_no`,`email`)"
                            + "VALUES ('" + name + "', '" + mobile + "', '" + vehicleno + "', '" + address + "', '" + formattedDate + "', '" + nic + "', '" + username + "', '" 
                            + password + "', '" + imageName + "', '" + licenseno + "','" + email + "')");

                    JOptionPane.showMessageDialog(this, "Distributor Registered Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    

    }//GEN-LAST:event_jButton1ActionPerformed

