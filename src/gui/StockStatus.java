    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
    
            String selectedStatus = (String) jComboBox1.getSelectedItem();

       
            if ("Select Status".equals(selectedStatus)) {
                JOptionPane.showMessageDialog(this, "Please select a valid status.");
                return;
            }

     
            String statusId = statusMap.get(selectedStatus);

      
            String query = "UPDATE `stock` SET `stock_status_id` = '" + statusId + "' WHERE `id` = '" + id + "'";

      
            int rowsUpdated = MySQL.executeIUD(query);

         
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Stock status updated successfully.");
                this.dispose(); 
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update stock status.");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage());
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

