    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String username = jTextField1.getText();
            String password = String.valueOf(jPasswordField1.getPassword());
            
            if (username.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter your Username", "Warning", JOptionPane.WARNING_MESSAGE);
                
            }else if (password.isEmpty()) {
                 JOptionPane.showMessageDialog(this,"Please enter your password","Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `w_supervisor` WHERE `username` = '"+username+"' AND `password` ='"+password+"'");
                
                if (resultSet.next()) {
                    //enter the location code here
                    JOptionPane.showMessageDialog(this, "Loged","Warning",JOptionPane.WARNING_MESSAGE);
                    
                } else {
                   JOptionPane.showMessageDialog(this, "Invalid username or password", "Warning", JOptionPane.WARNING_MESSAGE);
                   
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

